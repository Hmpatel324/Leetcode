"""
Link: https://leetcode.com/problems/concatenation-of-array
Difficulty: easy

Problem:
concat two lists

Input / Output:


Notes:


Solution Approaches:
list addition

"""

class Solution(object):
    def getConcatenation(self, nums):
        """
        :type nums: List[int]
        :rtype: List[int]
        """
        return nums + nums